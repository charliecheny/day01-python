
# 数学运算符
def math_operator(a,b):
    print(a + b)
    print(a - b)
    print(a * b)
    print(a / b)
    print(a % b)
# 比较运算符
def comp_operator(a,b):
    print(a > b)
    print(a < b)
    print(a == b)
    print(a >= b)
    print(a != b)

if __name__ == '__main__':
    math_operator(8,3)
    comp_operator(8,3)
